	<h2 class="custom" style="margin:0;"><b>PLACEMENT PROCEDURE</b></h2><hr class="stylish-rule" style="height:2.6px;">
	<div class="main-content">
		<h3></b>Pre-Placement Presentation</h3></b>
			<p>This is the first part of the procedure. In the pre-placement presentation, the companies that have come for the campus placements put up presentations regarding their organisation. You get to know all about the companies, their recruitment process and the salaries you can expect. After the presentations, students are allowed to ask questions to the representatives.</p>

			<h3></b>Qualifications</h3></b>
			<p>In this step, you will know about the qualifications and other important criteria companies have set for recruitment. Usually, companies interview only those candidates that belong to a certain stream of education and will choose from among them. Since colleges teach multiple disciplines, and placements drives for all disciplines are held together, make sure you know which companies are interested in your education.</p>

		  <h3></b>Written Examination</h3></b>
			<p>Once you know the companies you are going to be applying for, you will have to attempt an aptitude test. The test may vary based on the positions you are applying for, but there are a few tests specific to education streams. For example, during engineering placement drives, the Wheebox Graduate Employability Test is the standard test that most companies use. This exam tests coding skills, math skills, English language skills, and critical thinking skills. Similarly, there are other tests for other disciplines.</p>

		<h3></b>Group Discussion</h3></b>
			<p>A few times, this round is not conducted, but many companies use this for filtering candidates. A group of students is created that is moderated by a judge. A common topic is given for the group to discuss. Once the discussions begin, students are judged on their knowledge, their communication skills, their confidence, their leadership skills, and their listening and retaining capabilities. In the GD round, the latest news and current affairs are discussed, so you need to brush up on your knowledge.</p>

		<h3><b>Technical Knowledge Interview</h3></b>
		<p>
      This is an interview that will check your knowledge related to the subject you are studying, and other important technical knowledge that will be related to your profession. This round is mostly conducted in a one-on-one setting, but if there are many students and interviews are short on time, they may conduct interviews as a group.
      </p>

		<h3></b>Formal Interview</h3></b>
			<p>This is the final part of the procedure â€“ formal interview. In this round, you are judged for your confidence and abilities. You can even term this interview as your job interview. If you have done internships, or have worked on real industry projects, it will really help you do well during this interview. In this interview, you will most likely know if you have been selected.
      </p>

		<h3></b>Post Placement Discussion</h3></b>
				<p>If you have cleared the formal interview, you will get an offer letter and a post-placement discussion. You will be given guidelines and be told about the joining procedure. You will also be told any other important information you need to know.</p>

